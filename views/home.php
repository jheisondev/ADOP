                <div class="modal-header mx-auto">
                    <h5 class="modal-title text-center">23 Formas de exercer a cidadania além do voto</h5>
                </div>
                <div class="modal-body">
                    <p  class="text justify-content-center">
                    O voto é sempre apenas o primeiro passo da atuação de um cidadão engajado, pois existem diferentes maneiras de participar ativamente da política. Além das formas tradicionais de participação na política partidária (como candidatar-se a um cargo eletivo, filiar-se ou apoiar um partido político), é possível participar de um conjunto de espaços de participação social garantidos por lei, ou até mesmo criar novas formas e estratégias para influenciar as políticas e decisões públicas.
                    </p>
                    <p  class="text justify-content-center">
                    Abaixo estão listadas 23 maneiras para inspirar o exercício da cidadania em nível municipal, que é a instância mais próxima para exercitarmos a chamada democracia participativa. São apenas algumas possibilidades para se consagrar como um cidadão protagonista e ativo no controle social e na participação nas decisões públicas municipais.
                    </p>
                    <ul>
                        <li>Conhecer mais sobre os Conselhos temáticos da cidade e participar de algum deles, como o Conselho da Saúde, da Educação, do Meio Ambiente, entre outros.</li>
                        <li>Participar das reuniões do Orçamento Participativo (OP) para propor que as necessidades coletivas da minha região possam, de fato, entrar no orçamento público municipal. Caso não exista um OP em minha cidade, uma opção é ir à Câmara de Vereadores para propor a sua criação.</li>
                        <li>Acompanhar as Audiências Públicas de sua cidade, seja para discussão do orçamento público, para definições do planejamento urbano municipal, para licenças ambientais ou tantas outras questões relevantes. Quando houver outros assuntos de relevância social, propor a realização de mais audiências junto à Câmara de Vereadores.</li>
                        <li>Montar um grupo de acompanhamento das sessões legislativas que monitore de perto todo o trabalho realizado pelos vereadores – assim como faz o pessoal do Voto Consciente (de São Paulo ou Jundiaí, por exemplo), que possuem inclusive uma cartilha explicando o método utilizado. Ou simplesmente adotar um vereador para monitorar seu desempenho.</li>
                        <li>Ficar de olho nos Portais da Transparência (da Prefeitura, da Câmara de Vereadores e de Autarquias municipais), tanto para acompanhar as licitações, os gastos e as receitas, quanto para ver se as informações contidas estão de acordo com a Lei de Acesso a Informação (para municípios com até 10 mil habitantes).</li>
                        <li>Solicitar ao Serviço de Informação ao Cidadão as informações públicas que desejar receber para minha atuação cidadã. Este canal de comunicação entre governo e sociedade civil – que deve funcionar nos municípios brasileiros – tem a obrigação de disponibilizar dados públicos a todos os cidadãos interessados: tudo de acordo com a Lei de Acesso a Informação.</li>
                        <li>Organizar um “observatório cidadão” que possa acompanhar as metas municipais determinadas pela Prefeitura e monitorar as políticas públicas da cidade – algo inspirado nas iniciativas da Rede Brasileira por Cidades Justas e Sustentáveis, tais como: a Rede Nossa São Paulo, o Instituto Nossa Ilhéus, Ilhabela Sustentável etc.</li>
                        <li>Fomentar a organização de um grupo específico do Observatório Social do Brasil em minha cidade, cujo foco é o acompanhamento das compras e licitações públicas – a exemplo do que fazem várias cidades no país organizadas nesta rede.</li>
                        <li>Solicitar o compromisso do Prefeito e dos Vereadores com o Programa Cidades Sustentáveis, para que realizem a elaboração de metas municipais associadas a um conjunto de indicadores que, juntos, contribuem para uma gestão pública municipal mais sistêmica e efetiva.</li>
                        <li>Participar das Conferências temáticas que ocorrerão na cidade. Estas possuem o objetivo de debater e elaborar propostas de políticas públicas, em áreas como: direitos humanos, educação, idosos, saúde, mulheres, segurança, meio ambiente, assistência social etc. É preciso estar atento ao calendário, pois costumam ocorrer a cada dois anos.</li>
                        <li>Articular uma rede de mobilização local, com cidadãos ativos em realizar o controle social na cidade, aos moldes do que fazem as iniciativas da rede Nossas Cidades (como o Minha Sampa, Minha Porto Alegre etc) ou a Plataforma Engajados.</li>
                        <li>Adaptar ferramentas digitais já existentes (com licenças livres e códigos abertos disponíveis) para que passem a conter informações específicas sobre minha cidade e auxiliem no controle social, tal como o Login Cidadão, a DemocracyOs, o Legislando ou o De Olho nas Metas.</li>
                        <li>Utilizar alguns aplicativos cívicos e aproveitar a tecnologia a favor da participação e do controle social. Alguns exemplos de apps são: Colab, Cidadera, Monitorando a Cidade etc.</li>
                        <li>Articular ou engajar-se em coletivos ou movimentos sociais dos quais se identifique e, assim, provocar melhorias na cidade.</li>
                        <li>Provocar ações ativistas, vinculadas ou não a movimentos sociais, também são maneiras de buscar uma maior participação nas decisões públicas e na construção daquilo que é coletivo. A Escola de Ativismo e o Imagina.vc_napolítica, por exemplo, podem ajudar nessa inspiração.</li>
                        <li>Articular uma iniciativa coletiva em prol da fiscalização municipal, como o trabalho realizado pela Rede Amarribo, que conecta centenas de grupos de diferentes municípios com a missão de praticarem o controle social.</li>
                        <li>Realizar um concurso municipal que vise desenvolver e premiar soluções colaborativas para problemas municipais. Uma ação como essa pode ser viabilizada, por exemplo, por meio da plataforma Cidade Democrática.</li>
                        <li>Propor a criação de fóruns que discutam políticas públicas da cidade acerca de temas como, por exemplo: “resíduos sólidos”, “mobilidade urbana”, “habitação”, “transporte público”.</li>
                        <li>Utilizar a ouvidoria pública do governo municipal como um canal de denúncias ou sugestões de melhorias para a cidade.</li>
                        <li>Articular um grupo de estudos para monitorar alguma área/política pública da cidade (como o Transporte Público, por exemplo) e divulgar boletins para a população. Um exemplo é o trabalho do Observatório Cidadão de Piracicaba, como nesses boletins de Mobilidade Urbana e Transparência Pública.</li>
                        <li>Organizar ou participar de campanhas no Avaaz, Change.org ou no Panela de Pressão, com a consciência de que são ferramentas importantes, mas que é possível complementá-las com outras formas de participação social.</li>
                        <li>Estudar um pouco mais sobre Política e Cidadania e buscar formações especializadas (como este curso de Ciência Política online oferecido de forma gratuita pela USP) e também utilizar plataformas de educação política como o Meu Município ou o próprio Politize.</li>
                        <li>Levar educação política para minha cidade! Você pode, por exemplo, procurar a Escola de Cidadania Criativa do Instituto Terroá, o Pé na Escola ou Fast Food da Política.</li>
                    </ul>
                    <p  class="text justify-content-center">
                    As formas e estratégias de participação e controle social são infinitas. E é importante que estejam sempre conectadas àquilo que é coletivo. Isto é, a participação social, no campo democrático, precisa estar associada à ampliação de direitos, ao acesso à cidade e à inclusão social.
                    Então, essa é a hora de abrir a caixa da criatividade política e arregaçar as mangas para uma aproximação cada vez maior entre a sociedade civil e a gestão pública.
                    </p>
